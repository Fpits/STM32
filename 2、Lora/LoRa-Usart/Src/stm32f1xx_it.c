/**
  * @brief This function handles USART2 global interrupt.
  */
void USART2_IRQHandler(void)
{
  /* USER CODE BEGIN USART2_IRQn 0 */

  
    uint32_t tmp_flag = 0;
    uint32_t temp;
    if(USART2 == huart2.Instance)
	{
		//printf("interrupt\r\n");
		tmp_flag =__HAL_UART_GET_FLAG(&huart2,UART_FLAG_IDLE); //获取IDLE标志位
		if(tmp_flag != RESET)
		{

			usart2.recv_end_flag = 1;
			__HAL_UART_CLEAR_IDLEFLAG(&huart2);//清除标志位    
			HAL_UART_DMAStop(&huart2); 
			temp = __HAL_DMA_GET_COUNTER(&hdma_usart2_rx);// 获取DMA中未传输的数据个数
			usart2.rx_len = BUFFER_SIZE - temp; //总计数减去未传输的数据个数，得到已经接收的数据个数
			HAL_UART_Receive_DMA(&huart2,usart2.rx_buffer,BUFFER_SIZE);//重新打开DMA接收
			
		}

	}
  
  
  /* USER CODE END USART2_IRQn 0 */
  HAL_UART_IRQHandler(&huart2);
  /* USER CODE BEGIN USART2_IRQn 1 */

  /* USER CODE END USART2_IRQn 1 */
}

